package minsrc;

public class MinuteCalc {
	
	public static void main (String [] args)
	{
		//Define variable(s)
		double minute;
		
		//Calculate minutes in a(n)(earth)year.
		minute = 365.25*24*60;
		
		//Print out the final application output!
		System.out.print ("YAY! There are exactly ");
		System.out.print(minute);
		System.out.println (" minutes in one year!");
		System.out.println ("Isn't that just the most awesome fact ever?!");
	}

}
